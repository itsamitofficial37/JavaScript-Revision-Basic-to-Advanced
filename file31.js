// how to create objects

const person = {
    name : "Amit",
    age : 22,
    hobbies : ["Guitar","Sleeping","Listening Music","Eating"]
}

console.log(person);

// how to access data from object 

console.log(person.name);
console.log(person.hobbies);
console.log(person.age);

